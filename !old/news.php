      <h2>Die Praxis am Bahnhof erweitert ihre Öffnungszeiten      </h2>
      <p><strong>Die Praxis am Bahnhof in Rüti erweitert ihre Öffnungszeiten. Sie werden unter der Woche bis auf 22.00 Uhr ausgedehnt. Neu sind auch am Sonntag Sprechstunden möglich.</strong></p>
      <p>Mit der Erweiterung der Öffnungszeiten reagiert die Praxis am Bahnhof auf ein wachsendes Bedürfnis: Immer mehr Patienten wünschen Sprechstunden am Abend oder am Wochenende. &bdquo;Deshalb haben wir ab dem 1. November unter der Woche von 7.00 bis 22.00 Uhr geöffnet, samstags von 8.00 bis 18.00 Uhr. Aber auch am Sonntag sind wir für unsere Patienten da – von 10.00 bis 16.00 Uhr&ldquo;, sagt Christoph Zeller, der Geschäftsführer der Praxis am Bahnhof. &bdquo;Unser Team freut sich, mit diesem patientenfreundlichen Angebot nach langer Vorbereitung zu starten.&ldquo; Während den folgenden Zeiten ist auch eine Behandlung ohne Voranmeldung möglich: Unter der Woche von 8.00 bis 20.00 Uhr, am Samstag von 8.00 bis 18.00 Uhr. &bdquo;In den übrigen Zeiten verweisen wir auf den regionalen Notfalldienst oder auf die SOS-Ärzte mit denen wir zusammenarbeiten&ldquo;, so Zeller.      </p>
      <p><strong>Modernste Technik</strong><br />
      In der Praxis am Bahnhof in Rüti arbeiten Spezialärzte aus verschiedenen medizinischen Fachrichtungen unter einem Dach: Vertreten sind Spezialisten der Allgemeinmedizin, Inneren Medizin, Gynäkologie, Dermatologie, Chirurgie, Orthopädie inkl. Venenleiden und Psychiatrie. Auch Kinder werden medizinisch betreut. Die Praxis ist mit modernsten technischen Apparaturen ausgestattet, wie zum Beispiel einem digitalen Röntgengerät. Neu ist ein grosser Operationssaal dazugekommen, indem alle ambulanten Eingriffe durchgeführt werden können.</p>
