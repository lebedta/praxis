<p class="title1">Misteltherapien Teil 1</p>
<p class="title2">Wie Krebs entsteht</p>
<p>Jeden Tag sterben in unserem Körper Millionen von Zellen ab und es entstehen dafür neue, indem sich die Zellen fortwährend teilen. Bei der Teilung gibt sie den genauen Bauplan für das Gewebe aus dem die Zelle stammt an die neu entstehende Zelle weiter: wie diese auszusehen hat, was sie zu tun hat, wie oft sie sich teilen soll und wann sie abzusterben hat. Wenn bei diesem höchst komplizierten Prozeß etwas schief läuft, entsteht eine entartete Zelle. Diese kann absterben oder sie kann sich weiter teilen und immer mehr entartete Zellen produzieren – in diesem Fall entsteht ein Tumor, der eigentlich bloß ein Klumpen "falscher" Zellen ist.</p>
<p>Das Entstehen eines Tumors kann durch viele Faktoren begünstigt werden. Äußere (exogene) Faktoren wie Vitaminmangel, Alkohol, Nikotin, Schadstoffe in Nahrungsmitteln, Umweltgifte, Strahlung und Viren können beispielsweise die Zellteilung ungünstig beeinflussen. Auch innere (endogene) Faktoren können diesen Effekt haben, und nicht zuletzt weiss man, daß bestimmte Formen der Krebserkrankung erblich bedingt sind.</p>
<p>Wenn Sie sich vor Augen halten, daß täglich Millionen von Zellen den ihnen vorbestimmten Weg der Teilung und des Absterbens gehen, wird klar, daß auch jeden Tag irgendwo im Körper "Unfälle" passieren und Tumorzellen entstehen können. Wenn alles normal läuft, wird unser Immunsystem aber damit fertig. Nur wenn die entarteten Zellen zu viele sind oder das Immunsystem zu schwach, geht der tägliche Kampf verloren, und es entsteht aus einzelnen "schlechten" Zellen ein Tumor beziehungsweise Krebs.</p>
<p class="title2">Die drei Säulen der Konventionellen Krebstherapie</p>
<p>Die Zerstörung des Tumors ist das erste und wichtigste Ziel der konventionellen Krebsbehandlung. Hier gibt es die drei "klassischen Säulen": Operation, Bestrahlung und Chemotherapie.</p>
