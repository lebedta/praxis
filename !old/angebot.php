<p class="title1">Anthroposophisch Erweiterte Medizin</p>
<p>Die anthroposophische Medizin basiert auf der naturwissenschaftlichen Medizin und wird nur von ausgebildeten Aerzten ausgeübt. Sie fügt zu den naturwissenschaftlichen Erkenntnissen die Forschungsergebnisse über das Wesen des menschlichen Lebens, der Seele und des Geistes hinzu.<br />
  Das Vorgehen eines anthroposophischen Arztes unterscheidet sich bei der Diagnose einer Erkrankung nur wenig von der konventionellen Medizin. Der Aspekte der Psyche und der Seele werden noch vermehrt miteinbezogen.</p>
<p class="title2">Ausbildung und wissenschaftliche Grundlagen:</p>
<p>Die Ausbildung in anthroposophischer Medizin basiert auf dem regulären Medizinstudium sowie der fachärztlichen Weiterbildung. Hinzu kommt das Studium der anthroposophischen Geisteswissenschaft und ihrer Forschungsmethoden sowie die Ausbildung künstlerischer Fähigkeiten und Grundkenntnisse in Heileurythmie und künstlerischer Therapie.</p>
<p class="title2">Das therapeutische Spektrum:</p>
<p>Das therapeutische Spektrum reicht über die Methoden der Schulmedizin über anthroposophische Heilmittel bis zu den Kunsttherapien. Viele anthroposophische Heilmittel sind pflanzlichen und mineralischen Ursprungs.<br />
  Die Ausübung der anthroposophischen Medizin umfasst neben der allgemeinmedizinischen, hausärztlichen Versorgung, alle medizinischen Disziplinen von der Chirurgie über die innere Medizin, Pädiatrie, Onkologie, Frauen- und Geburtsheilkunde bis hin zur Neurologie und Psychiatrie.</p>
<p class="title2">Geschichte und derzeitige Verbreitung:</p>
<p>Die anthroposophische Medizin wurde zu Beginn der zwanziger Jahre von Dr. med. Ita Wegman und anderen Ärzten in Zusammenarbeit mit Dr. phil. Rudolf Steiner begründet.<br />
  1921 entstanden in Stuttgart/Deutschland und Arlesheim/Schweiz erste klinisch-therapeutische Institute mit angegliederten pharmazeutischen Laboratorien, den Vorläufern der späteren Arzneimittelbetriebe Weleda und Wala. Die anthroposophische Medizin hat sich von Anfang an als ein in die Methoden der Schulmedizin integrierendes Konzept verstanden.</p>
<p class="title2">Ethik und Selbstverständnis:</p>
<p>Im Zentrum stehen der Patient und die individuelle Therapie. Aus diesem Grunde kommen Therapieschemata nur ausnahmsweise zur Anwendung. Lebensalter, Konstitution, biographische Situation und die seelisch-geistige Befindlichkeit werden in Diagnostik und Therapie miteinbezogen.</p>
