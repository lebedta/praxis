<p class="title1">Misteltherapien Teil 2</p>
<p class="title2">Die Misteltherapie</p>
<p>Die Misteltherapie kann nun zu diesen Therapien als Zusatztherapie angewandt werden. Im Prinzip ist sie mit allen obigen Methoden kombinierbar. Je nach Tumortyp und Grad der Tumorerkrankung, ist eine andere Kombination sinnvoll.</p>
<p>Die Mistel hat einerseits einen direkt abtötenden Effekt auf die Krebszellen, andererseits eine stimulierende Wirkung auf das Immunsystem, unter anderem auf die Lymphocyten (insbesondere die T-Lymphocyten die Killerzellen weiter aber auch auf die B- Lymphocyten + die humorale Immunantwort).</p>
<p>In einem Gesamtextrakt aus der Mistel hat es verschiedene Inhaltsstoffe die dann zu verschiedenen Medikamenten in Ampullenform verarbeitet werden. Von den Verschiedenen Mistelmedikamenten, sucht man das aus welches für die entsprechende Tumorart Am wirksamsten ist. Meist wird das Präparat dann 2-3 x / Woche unter die Haut gespritzt. Dies ist relativ einfach und von den meisten Patienten in kurzer Zeit zu erlemen.</p>
<p>Mistelpräparate können auch als Infusion verabreicht werden, was wir aber deutlich seltener machen als die einfache subcutane Injektion.</p>
<p>Reaktion auf die Injektion:<br />
In der Regel kommt es kurz nach der Injektion, vor allem zu Beginn der Therapie, zu einer so genannten Lokalreaktion: Die Haut um die Einstichstelle rötet sich und kann kurzzeitig etwas jucken, leicht anschwellen und sich erwärmen. Nicht bei jedem Patienten und nicht nach jeder Injektion treten Lokalreaktionen auf. Wir beobachten die Reaktionen auf die ersten Injektionen und reagieren, wenn diese zu heftig ausfallen.</p>
