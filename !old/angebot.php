<p class="title1">Misteltherapien Teil 3</p>
<p class="title2">Die Mistel</p>
<p>Die besondere Eigenart der Mistel ist, dass sie als Schmarotzerpflanze auf ihren Wirtsbäumen lebt. Verschiedene Vogelarten fressen die weißen Beeren, die einen Samen enthalten. Entweder scheiden die Vögel den Samen wieder aus und er bleibt mit dem Vogelkot an einem Ast kleben, oder sie fressen nur das Fruchtfleisch und kleben den Samen dabei an einen Ast oder Stamm. Der Samen bildet nun einen so genannten Senker aus, der langsam vom Gehölz umwachsen wird und so in der Lage ist, die keimende Mistel mit Wasser und Nährstoffen zu versorgen. Ausgesprochen langsam entwickelt sich aus dem Keimling der Mistelbusch mit seiner typischen, kugeligen Gestalt, und erst nach fünf bis sieben Jahren beginnt die Mistel zu blühen. Erst im Alter von 10- 15 Jahren wird sie geerntet und zu einem pharmazeutischen Präparat verarbeitet.</p>
<p>Es gibt verschiedene Arten von Misteln, u.a.:<br />
- Die Apfelbaummistel<br />
- Die Kiefermistel<br />
- Die Tannenmistel</p>
<p>Die Forschung hat ergeben, dass die Mistel im Sommer einen hohen Gehalt an Viscotoxinen hat, im Winter ist dagegen der Gehalt an Mistellektinen sehr viel höher. Also erntet man zweimal jährlich, im Frühjahr und im Herbst, und gewinnt so Säfte derselben Mistelart, aber mit jeweils unterschiedlichen Inhaltsstoffen. Die geernteten Misteln werden in einem genau festgelegten Verfahren zu einem Extrakt verarbeitet. Die Sommer- und Wintersäfte werden ebenfalls in einem speziellen Verfahren gemischt. Das so entstandene Präparat enthält also einen ausgewogenen Anteil beider Stoffgruppen.</p>
