      <h2>Krampfadern - Behandlung durch Die Schaumverödung</h2>
      <p>Nach gründlicher Untersuchung des Venensystems mit einem
Ultraschallgerät (Farbdoppler) wird eine geringe Menge eines seit über 40 Jahren bekannten Verödungsmittels in Form von Schaum unter sonographischer Sicht in die kranke Vene injiziert. Die Krampfader zieht sich zusammen und steht für den Blutabfluss nicht mehr zur Verfügung, denn dieser erfolgt nun über das tiefe Venensystem. Anschließend zieht der Patient Kompressionsstrümpfe an und bleibt maximal für zwei Stunden zur Kontrolle in der Praxis. Das Ergebnis wird mit Ultraschall kontrolliert und die nächste Behandlung beginnt. Normalerweise sind je nach Schweregrad der Erkrankung drei bis fünf Behandlungen pro Bein nötig.</p>
