      <h2>Mammakarzinom – Neue Aspekte zur Diagnostik und Therapie</h2>
      <p>Brustkrebs ist heute die am häufigsten zum Tode führende Krebserkrankung bei Frauen. In der jüngsten 
        Vergangenheit wurden zahlreiche neue Untersuchungstechniken und Behandlungsmöglichkeiten entwickelt 
        und etabliert.<br />
      <br />
      In dieser Neuerscheinung hat der Herausgeber neueste Konzepte zum Mammakarzinom gut verständlich zusammengestellt. Im Vordergrund steht dabei der praktische Nutzen für die erkrankten Frauen. Individuell ausgerichtetes Vorgehen zur Kontrolle der Krankheit und zur Erhaltung der Lebensqualität sind zentrale Inhalte des Buches.<br />
      <br />
      Das vorliegende Werk bietet einen guten Überblick über aktuelle Entwicklungen zum Mammakarzinom. <br />
      Ärzte in Klinik und Praxis, andere Berufsgruppen, die in die Versorgung von Brustkrebspatientinnen einge- <br />
      bunden sind, sowie Patientinnen und Angehörige können ihr  Wissen auf den neuesten Stand bringen.<br />
      <br />
      Inhalt: <br />
      <blockquote>1. Operative Diagnostik und Therapie beim Mammakarzinom: Aktuelle und neue Aspekte <br />
        2. Erweiterte pathologische Diagnostik: Was ist neu? <br />
        3. Adjuvante Therapieentscheidungen beim Mammakarzinom <br />
        4. Implantierbare Portsysteme zur intravenösen Therapie <br />
        5. Manuelle Lymphdrainage nach Brustkrebs-Operation <br />
        6. PET-CT und Mammakarzinom <br />
        7. Neurochirurgische Behandlungsmöglichkeiten bei ZNS-Metastasen <br />
        8. Lebensqualität von Patientinnen mit Mammakarzinom </blockquote>
      </p>
