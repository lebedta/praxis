      <p>Die Praxis am Bahnhof ging aus der Praxis Dr. Christoph Zeller hervor, welche im Jahre 1996 in Rüti neu gegründet wurde.<br />
        2006 wurde die Praxis in einen Neubau direkt am Bahnhof Rüti verlegt und stark vergrössert.<br />
        Die Physiotherapie wurde ausgegliedert und als eigenständige Firma geführt.<br />
        In den letzten Jahren sind immer mehr Tätigkeiten und neue Fachbereiche dazu gekommen. Anfänglich waren wir eine Praxis ausschliesslich für allgemeine Medizin. Mit der Zeit sind nun Gynäkologie, Dermatologie, Chirurgie, Orthopädie inkl. Venenleiden und Gesprächstherapie sowie Psychiatrie dazugekommen.</p>
      <h2>Leitbild</h2>
      <p>Wir positionieren uns als modernes Dienstleistungsunternehmen im Gesundheitswesen, das mit den vorhandenen Ressourcen für den Patienten eine qualitativ hochstehende, moderne Medizin betreibt.</p>
      <p>Wir betrachten den Patienten als Partner und Kunden und versuchen seine Anliegen, soweit es uns möglich ist, zu berücksichtigen.</p>
      <p>Wir sind neuen Ideen und Erkenntnissen aufgeschlossen, wir versuchen innovative Ideen umzusetzen.</p>
      <p>Wir bieten dem Patienten volle Aufklärung über seine Situation und versuchen mit ihm zusammen eine für ihn optimale Lösung zu finden.</p>
      <p>Wir sind unseren Mitarbeitern gegenüber fair und schaffen ein leistungsorientiertes Arbeitsklima. Durch gute Leistung aller Mitarbeiter erwirtschaften wir gute Löhne.</p></td>
